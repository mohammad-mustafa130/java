class switchdemo {
	public static void main(String[] args) {
		char ch = 'B';
		switch(ch) {
			case 'A':
				System.out.println("Case A");
				break;
			case 'B':
				System.out.println("Case B");
				break;
			case 'C':
				System.out.println("Case C");
				break;
			default: 
				System.out.println("Default Case");
		}
	}
}
